

def transformador(frase:str) -> list:
    """Transforma uma frase em uma lista, separando a frase pelos espaços em branco.
    
    Arguments:
        frase {str} -- [A frase a ser transformada em uma lista]
    
    Returns:
        list -- [Uma lista com o conteúdo da frase separada por whitespaces]
    """


    return frase.split(" ")